#ifndef __VKTYPE_H__
#define __VKTYPE_H__

#include "../ptools.h"

typedef union vitual_key_dataObj
{
    struct
    {
        word    dummy      : 8; /* 가상글쇠 코드      */
        word    right_shift : 1; /* 오른쪽 쉬프트 눌림 */
        word    left_shift  : 1; /* 왼쪽 쉬프트 눌림   */
        word    right_ctrl  : 1; /* 오른쪽 콘트롤 눌림 */
        word    left_ctrl   : 1; /* 왼쪽 콘트롤 눌림   */
        word    right_alt   : 1; /* 오른쪽 알트 눌림   */
        word    left_alt    : 1; /* 왼쪽 알트 눌림     */
        word    caps_lock   : 1; /* Caps Lock 작동     */
        word    num_lock    : 1; /* Num Lock 작동      */
    } i;
    struct
    {
        byte   vkey_code     ; /* 가상글쇠 코드     */
        byte   modifier_state; /* 바꿈 글쇠 상태    */
    } h;
    word16 vkey_data;
}
virtual_key_dataRec;

typedef struct key_inputObj
{
    word16              input_kind; /* 입력 종류 */
    virtual_key_dataRec vkey_data  ; /* 가상글쇠 입력 */
    letter              code   ; /* 글자부호 */
} key_inputRec;

#endif
